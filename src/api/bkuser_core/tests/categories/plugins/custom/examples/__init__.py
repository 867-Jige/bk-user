import sys
from http.server import CGIHTTPRequestHandler, HTTPServer


def main(ServerClass=HTTPServer, protocol="HTTP/1.0", port=8000, bind=""):
    """This runs an CGI HTTP server on port 8000 (or the port argument)."""
    server_address = (bind, port)

    CGIHTTPRequestHandler.protocol_version = protocol
    with ServerClass(server_address, CGIHTTPRequestHandler) as httpd:
        sa = httpd.socket.getsockname()
        serve_message = "Serving HTTP on {host} port {port} (http://{host}:{port}/) ..."
        print(serve_message.format(host=sa[0], port=sa[1]))
        try:
            httpd.serve_forever()
        except KeyboardInterrupt:
            print("\nKeyboard interrupt received, exiting.")
            sys.exit(0)


if __name__ == "__main__":
    main()
